DROP TABLE IF EXISTS COURSES;
CREATE TABLE COURSES
(
    ID          BIGINT GENERATED BY DEFAULT AS IDENTITY,
    NAME        VARCHAR(255),
    CATEGORY    VARCHAR(255),
    RATING      INT NOT NULL,
    DESCRIPTION VARCHAR(255),
    PRIMARY KEY (ID)
);


DROP TABLE IF EXISTS CT_USERS;
CREATE TABLE CT_USERS
(
    ID               BIGINT       NOT NULL,
    EMAIL            VARCHAR(255) NOT NULL,
    FIRST_NAME       VARCHAR(255) NOT NULL,
    LAST_NAME        VARCHAR(255) NOT NULL,
    PASSWORD         VARCHAR(255) NOT NULL,
    USERNAME         VARCHAR(255) NOT NULL,
    VERIFIED         BOOLEAN      NOT NULL,
    LOCKED           BOOLEAN      NOT NULL,
    ACC_CRED_EXPIRED BOOLEAN      NOT NULL,
    PRIMARY KEY (ID),
    CONSTRAINT uk_ct_users_username UNIQUE (USERNAME)
);

CREATE TABLE CT_AUTHORITIES
(
    USERNAME  VARCHAR(255) NOT NULL,
    AUTHORITY VARCHAR(50) NOT NULL,
    CONSTRAINT pk_ct_authorities PRIMARY KEY (USERNAME, AUTHORITY),
    CONSTRAINT fk_ct_authorities__ct_users
        FOREIGN KEY (USERNAME) REFERENCES CT_USERS (USERNAME)
);
